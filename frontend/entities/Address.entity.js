class Address {
    constructor({
      id,
      street,
      city,
      state,
      zipCode,
      country,
    }) {
      this.id = id;
      this.street = street;
      this.city = city;
      this.state = state;
      this.zipCode = zipCode;
      this.country = country;
    }
  }
  
  export default Address;